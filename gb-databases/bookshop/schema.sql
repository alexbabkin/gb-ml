CREATE TABLE books (
  id INT UNSIGNED NOT NULL AUTO_INCREMENT PRIMARY KEY,
  name VARCHAR(255) NOT NULL,
  publication_year YEAR,
  publication_name VARCHAR(255),
  is_audio BOOL NOT NULL,
  isbn VARCHAR(13),
  rating FLOAT NOT NULL,
  price DECIMAL(10, 2) NOT NULL,
  discont FLOAT,
  description TEXT
) COMMENT = 'книги';

CREATE TABLE users (
  id INT UNSIGNED NOT NULL AUTO_INCREMENT PRIMARY KEY,
  email VARCHAR(100) NOT NULL,
  created_at DATETIME NOT NULL,
  promo_id INT UNSIGNED
) COMMENT = 'пользователи';

CREATE TABLE promos (
  id INT UNSIGNED NOT NULL AUTO_INCREMENT PRIMARY KEY,
  discont FLOAT NOT NULL,
  available_from DATE NOT NULL,
  available_until DATE 
) COMMENT = 'промокоды';

CREATE TABLE comments (
  id INT UNSIGNED NOT NULL AUTO_INCREMENT PRIMARY KEY,
  user_id INT UNSIGNED NOT NULL,
  book_id INT UNSIGNED NOT NULL,
  comment TEXT,
  created_at DATETIME NOT NULL,
  rating FLOAT NOT NULL
) COMMENT = 'комментарии';

CREATE TABLE text_metadata (
  id INT UNSIGNED NOT NULL AUTO_INCREMENT PRIMARY KEY,
  book_id INT UNSIGNED NOT NULL,
  page_count INT
) COMMENT = 'метаданные текстовых книг';

DROP TABLE IF EXISTS audio_metadata;
CREATE TABLE audio_metadata (
  id INT UNSIGNED NOT NULL AUTO_INCREMENT PRIMARY KEY,
  book_id INT UNSIGNED NOT NULL,
  duration TIME
) COMMENT = 'метаданные аудио книг';

CREATE TABLE audio_formats (
  id INT UNSIGNED NOT NULL AUTO_INCREMENT PRIMARY KEY,
  name VARCHAR(10) NOT NULL
) COMMENT = 'аудио форматы';

CREATE TABLE text_formats (
  id INT UNSIGNED NOT NULL AUTO_INCREMENT PRIMARY KEY,
  name VARCHAR(10) NOT NULL
) COMMENT = 'текстовые форматы';

CREATE TABLE authors (
  id INT UNSIGNED NOT NULL AUTO_INCREMENT PRIMARY KEY,
  first_name VARCHAR(20),
  second_name VARCHAR(20) NOT NULL
) COMMENT = 'авторы';

CREATE TABLE genres (
  id INT UNSIGNED NOT NULL AUTO_INCREMENT PRIMARY KEY,
  name VARCHAR(20) NOT NULL
) COMMENT = 'жанры';

CREATE TABLE tags (
  id INT UNSIGNED NOT NULL AUTO_INCREMENT PRIMARY KEY,
  name VARCHAR(20) NOT NULL
) COMMENT = 'теги';

CREATE TABLE book_authors (
  id INT UNSIGNED NOT NULL AUTO_INCREMENT PRIMARY KEY,
  book_id INT UNSIGNED NOT NULL,
  author_id INT UNSIGNED NOT NULL
) COMMENT = 'авторы книг';

CREATE TABLE book_genres (
  id INT UNSIGNED NOT NULL AUTO_INCREMENT PRIMARY KEY,
  book_id INT UNSIGNED NOT NULL,
  genre_id INT UNSIGNED NOT NULL
) COMMENT = 'жанры книг';

CREATE TABLE book_tags (
  id INT UNSIGNED NOT NULL AUTO_INCREMENT PRIMARY KEY,
  book_id INT UNSIGNED NOT NULL,
  tag_id INT UNSIGNED NOT NULL
) COMMENT = 'теги книг';

CREATE TABLE text_book_formats (
  id INT UNSIGNED NOT NULL AUTO_INCREMENT PRIMARY KEY,
  book_id INT UNSIGNED NOT NULL,
  text_format_id INT UNSIGNED NOT NULL
) COMMENT = 'форматы текстовых книг';

CREATE TABLE audio_book_formats (
  id INT UNSIGNED NOT NULL AUTO_INCREMENT PRIMARY KEY,
  book_id INT UNSIGNED NOT NULL,
  audio_format_id INT UNSIGNED NOT NULL
) COMMENT = 'форматы аудио книг';

CREATE TABLE user_purchased_books (
  id INT UNSIGNED NOT NULL AUTO_INCREMENT PRIMARY KEY,
  book_id INT UNSIGNED NOT NULL,
  user_id INT UNSIGNED NOT NULL
